package operator;

public class Main02 {

	public static void main(String[] args) {
		// 나눗셈
		// 0으로 나누기
		int num = 100;
		int zero = 0;
		int result1 = num / zero;
		// 빨간색 안뜨는 이유는 int/int이기 때문에 문법상으로 오류가 안뜨지만 실행할때는 무조건 오류가 난다.
		
		System.out.println(result1);

			// 이런경를 인셉션(?)이라고 한다.
		    // 조건문을 배우면서 저런걸 피해가는 경우를 배울 수 있을것.
		
		
	}

}
